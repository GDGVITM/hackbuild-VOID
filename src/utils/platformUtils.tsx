export const getPlatformIcon = (platform: string): React.ComponentType<IconProps> => {
  switch (platform) {
    case 'twitter':
      return TwitterIcon;
    case 'reddit':
      return RedditIcon;
    case 'facebook':
      return FacebookIcon;
    case 'instagram':
      return InstagramIcon;
    case 'tiktok':
      return TikTokIcon;
    default:
      return TwitterIcon;
  }
};

export const getPlatformColor = (platform: string) => {
  switch (platform) {
    case 'twitter':
      return { class: 'platform-twitter' };
    case 'reddit':
      return { class: 'platform-reddit' };
    case 'facebook':
      return { class: 'platform-facebook' };
    case 'instagram':
      return { class: 'platform-instagram' };
    case 'tiktok':
      return { class: 'platform-tiktok' };
    default:
      return { class: 'platform-default' };
  }
};
